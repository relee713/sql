CREATE DATABASE fit;
USE fit;

CREATE TABLE membership(
mshpid CHAR(3), 
mshpname VARCHAR(20), 
mshpprice NUMERIC(9, 2), 
PRIMARY KEY (mshpid));

CREATE TABLE member(
memid CHAR(3) NOT NULL, 
memname VARCHAR(20), 
memzip CHAR(5), 
msdatepayed DATE, 
mshpid CHAR(3), 
PRIMARY KEY (memid),
FOREIGN KEY (mshpid) REFERENCES membership(mshpid));

CREATE TABLE salestransaction(
stid CHAR(3) NOT NULL, 
memid CHAR(3), 
saledate DATE, 
PRIMARY KEY (stid),  
FOREIGN KEY (memid) REFERENCES member(memid));

CREATE TABLE merchandise(
mrchid CHAR(3) NOT NULL, 
mrchname VARCHAR(20), 
mrchprice NUMERIC(9, 2),
PRIMARY KEY (mrchid));

CREATE TABLE soldvia(
stid CHAR(3) NOT NULL,
mrchid CHAR(3) NOT NULL,
quantity INT, 
PRIMARY KEY (stid, mrchid), 
FOREIGN KEY (stid) REFERENCES salestransaction(stid),
FOREIGN KEY (mrchid) REFERENCES merchandise(mrchid));

CREATE TABLE daypasscat(
passcatid CHAR(3) NOT NULL, 
catname VARCHAR(20), 
price NUMERIC(9, 2), 
PRIMARY KEY (passcatid));

CREATE TABLE daypass(
passid CHAR(3) NOT NULL, 
passdate DATE, 
passcatid CHAR(3), 
memid CHAR(3), 
PRIMARY KEY (passid), 
FOREIGN KEY (passcatid) REFERENCES daypasscat(passcatid), 
FOREIGN KEY (memid) REFERENCES member(memid));

